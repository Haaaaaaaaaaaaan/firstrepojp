def main():
    st.title("🗾 일본 도시 관광지 안내 🧳")

    # 도시 선택
    city = st.selectbox("🎌 가고 싶은 도시를 선택하세요:", list(city_coordinates.keys()))

    # 관광지 정보 표시
    st.markdown(f"## ✨ {city} 여행 정보")
    st.info(city_attractions.get(city, "🎒 즐거운 여행 되세요!"))

    # 지도 생성 (선택된 도시 중심 확대)
    lat, lon = city_coordinates[city]
    m = folium.Map(location=(lat, lon), zoom_start=10)

    # 마커 추가 (선택된 도시만)
    folium.Marker(
        location=(lat, lon),
        popup=folium.Popup(f"<b>{city}</b><br>{city_attractions.get(city)}", max_width=250),
        tooltip="📍 여기예요!",
        icon=folium.Icon(color='red', icon='info-sign')
    ).add_to(m)

    # 지도 출력
    st_folium(m, width=700, height=500)

# 앱 실행
if __name__ == "__main__":
    main()
